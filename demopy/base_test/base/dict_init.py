# =====================================0=======================================
dit = dict()
print(dit)

# =====================================一=======================================
dit = {}  # 比较快  比dict()快
dit['a'] = 1
dit['b'] = 2
dit['c'] = 3
print(dit)

# =====================================2=======================================
dit = {'a': 1, 'b': 2}
print(dit)

# =====================================3=======================================
dit = dict(a=1, b=2, c='c')
print(dit)
# =====================================4=======================================

dit = dict(zip([1, 2, 3], ['a', 'b', 'c']))
print(dit)
# =====================================5=======================================
dit = dict.fromkeys(range(2), 3)
print(dit)
# =====================================6=======================================
lit = [('a', 1), ('b', 2), ('c', 3)]
dit = dict(lit)
print(dit)
# =====================================other======================================
dit = {}
result = dit.setdefault('four')
"""
1, setdefault参数为字典的键
2, 如果dit里面没有four 则 four的键值为None
3, 如果存在four键则返回该键值 """

